#Strings
print("Hola mundo")
print('Hola mundo')
print('''Hola mundo''')
print("""Hola mundo""")

print(type("Hola mundo"))

print("Adios " + "mundo")

#Numeros

#Integer
print(30)
#Float
print(30.1)

#boolean
print(type(True))
print(type(False))

#List
print(type([1, 2]))
print(type(['Hola', 'Adios']))
print(type([1, 'dos']))

#Tupla (No se pueden modificar)
print(type((1, 2)))

#Diccionario
print(type({"nombre":"Juan",
"apellidos":"crespo",
"genero":"varon"}))

#Nonetype
print(type(None))